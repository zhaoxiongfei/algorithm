// No 952. 按公因数计算最大组件大小

/**
  给定一个由不同正整数的组成的非空数组 A，考虑下面的图：

  有 A.length 个节点，按从 A[0] 到 A[A.length - 1] 标记；
  只有当 A[i] 和 A[j] 共用一个大于 1 的公因数时，A[i] 和 A[j] 之间才有一条边。
  返回图中最大连通组件的大小。

  示例 1：

  输入：[4,6,15,35]
  输出：4

  示例 2：

  输入：[20,50,9,63]
  输出：2

  示例 3：

  输入：[2,3,6,7,4,12,21,39]
  输出：8

  提示：

  1 <= A.length <= 20000
  1 <= A[i] <= 100000
 */

const getFactors = x => {
  const ans = [];
  for (let i = 2; i * i <= x; i += 1) {
    if (!(x % i)) {
      ans.push(i);
      while (!(x % i)) x /= i;
    }
  }
  if (1 < x) ans.push(x);

  return ans;
};

/**
 * @param {number[]} A
 * @return {number}
 */
const largestComponentSize = A => {
  const { length } = A;
  const map1 = new Map(); // 存储因子对应的数字下标集合
  const map2 = Array(length); // 存储数字对应的因子集合

  const pairNums = () => {
    for (let i = 0; i < length; i += 1) {
      map2[i] = getFactors(A[i]);
      for (const x of map2[i]) {
        const set = map1.get(x) || new Set();
        if (set.size === 0) map1.set(x, set);
        set.add(i);
      }
    }
  };

  const dfs = (key, set, exists = new Set()) => {
    exists.add(key);
    for (const x of map1.get(key)) {
      set.add(x);
      for (const k of map2[x]) {
        if (exists.has(k)) continue;
        dfs(k, set, exists);
      }
    }
  };

  pairNums();

  let max = 0;
  const exists = new Set();
  for (const [key] of map1) {
    if (exists.has(key)) continue;
    const set = new Set();
    dfs(key, set, exists);
    if (max < set.size) {
      max = set.size;
      // 如果结果达到一半则中断，不可能有比这个更长的了，剪枝操作
      if (length >> 1 <= max) break;
    }
  }

  return max;
};

