// https://leetcode.com/problems/count-of-smaller-numbers-after-self/discuss/76584/Mergesort-solution
// The smaller numbers on the right of a number are exactly
// those that jump from its right to its left during a stable sort.
// So I do mergesort with added tracking of those right-to-left jumps.
const countSmaller1 = nums => {
  const { length } = nums;
  if (length === 0) return [];

  const smaller = Array(length).fill(0);
  const sort = list => {
    if (list.length < 2) return list;
    const mi = list.length >> 1;
    const left = sort(list.slice(0, mi));
    const right = sort(list.slice(mi));
    for (let i = list.length - 1; i >= 0; i -= 1) {
      const ll = left[left.length - 1];
      const rl = right[right.length - 1];
      if (!rl || (ll && ll[1] > rl[1])) {
        smaller[ll[0]] += right.length;
        list[i] = left.pop();
      } else {
        list[i] = right.pop();
      }
    }

    return list;
  };

  sort(nums.map((x, i) => [i, x]));
  return smaller;
};

// 排序的思路二, 从后往前，依次采用插入的方式组织一个有序列表, 返回每次插入时的索引位置
const countSmaller2 = nums => {
  const { length } = nums;
  if (length === 0) return [];

  const dp = Array(length).fill(0);
  const list = [];

  const insert = (e, lo = 0, hi = list.length) => {
    // 在有序向量的区间[lo, hi)查找元素不大于 e 的元素的最大秩, 0 <= lo <= hi <= size
    while (lo < hi) {
      const mi = (lo + hi) >> 1; // 寻找中点
      // 经比较后确认深入 [lo, mi) 或 [mi + 1, hi)
      if (e <= list[mi]) hi = mi;
      else lo = mi + 1;
    }
    // 查找成功不能提前终止

    list.splice(lo, 0, e);
    return lo;
  };
  for (let i = length - 1; i >= 0; i -= 1) {
    dp[i] = insert(nums[i]);
  }

  return dp;
};

