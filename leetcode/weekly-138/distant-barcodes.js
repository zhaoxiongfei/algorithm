/**
 * @param {number[]} barcodes
 * @return {number[]}
 */
const rearrangeBarcodes = barcodes => {
  const { length } = barcodes;
  const map = new Map();
  for (const n of barcodes) {
    map.set(n, (map.get(n) || 0) + 1);
  }
  const stats = Array.from(map).sort((a, b) => b[1] - a[1]);
  const ans = Array(length);
  for (let i = 0; i < length; i += 1) {
    let j = 0;
    if (stats[0][0] === ans[i - 1]) j = 1;

    ans[i] = stats[j][0];
    stats[j][1] -= 1;
    j += 1;
    while (j < stats.length) {
      if (stats[j][1] <= stats[j - 1][1]) break;
      const t = stats[j];
      stats[j] = stats[j - 1];
      stats[j - 1] = t;
      j += 1;
    }
  }

  return ans;
};

