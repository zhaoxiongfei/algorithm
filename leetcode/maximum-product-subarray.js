// https://leetcode-cn.com/problems/maximum-product-subarray/description/
// No 152. 乘积最大子序列

/**
  给定一个整数数组 nums ，找出一个序列中乘积最大的连续子序列（该序列至少包含一个数）。

  示例 1:

  输入: [2,3,-2,4]
  输出: 6
  解释: 子数组 [2,3] 有最大乘积 6。
  示例 2:

  输入: [-2,0,-1]
  输出: 0
  解释: 结果不能为 2, 因为 [-2,-1] 不是子数组。
 */

/**
 * @param {number[]} nums
 * @return {number}
 */
// 思路：动态规划 令 dp[i][j] 表示从第 i 项到 第 j 项的乘积
const maxProduct1 = nums => {
  const { length } = nums;

  let max = nums[0];
  for (let i = 0; i < length; i += 1) {
    const dp = [];
    dp[i] = nums[i];
    if (dp[i] > max) max = dp[i];
    for (let j = i + 1; j < length; j += 1) {
      dp[j] = nums[j] * dp[j - 1];
      if (dp[j] > max) max = dp[j];
    }
  }

  return max;
};

const _maxProductWithoutZero = nums => {
  const { length } = nums;
  if (length === 0) return -Infinity;
  if (length === 1) return nums[0];

  // 收集所以负数出现的位置
  const stack = [];
  for (let i = 0; i < length; i += 1) if (nums[i] < 0) stack.push(i);

  // 如果负数的个数是偶数个，那没什么好说的，最大值就是所有的乘积一起
  if (stack.length % 2 === 0) {
    let i = 1;
    let max = nums[0];
    while (i < length) {
      max *= nums[i];
      i += 1;
    }
    return max;
  }

  // 如果是奇数个，那么相当于要舍弃一个负数，那么舍弃的负数的位置只能是 1, 3, 5, 7 这样的位置
  // 这里利用动态规划减少计算量
  // dp1[i] 记录截止到第 i 位之前的总乘积(包括第i位)
  // dp2[i] 记录从第 i 位开始之后的总乘积(包括第i位)
  const dp1 = Array(length + 1).fill(1);
  const dp2 = Array(length + 1).fill(1);
  for (let i = 1; i < length; i += 1) {
    dp1[i] = dp1[i - 1] * nums[i - 1];
    dp2[length - i] = dp2[length - i + 1] * nums[length - i];
  }

  let max = -Infinity;
  for (let i = 0; i < stack.length; i += 2) {
    const index = stack[i];
    if (dp1[index] > max) max = dp1[index];
    if (dp2[index + 1] > max) max = dp2[index + 1];
  }
  return max;
};

// 思路二: 上面的方法不够高效基本上是 O(n²) 的时间复杂度
// 分析：题目给定的条件是整数，这样的话，除了零之外，其余的数相乘都不会使得绝对值变小。
// 那么其余要考虑的就是负号个数的奇偶性
// 所以，找到 0 的那些点，
// 分别求各区域的最大连续乘积
// 这样各区域就不包含零了。那么找到包含偶数个负数的最大区域即可
// 一个小技巧，遇到负数入栈，再次遇到负数站最后的负数转为正数
const maxProduct = nums => {
  const { length } = nums;
  if (length === 0) return -Infinity;
  if (length === 1) return nums[0];

  let start = 0;
  while (nums[start] === 0 && start < length - 1) start += 1;
  let end = start;
  while (nums[end] !== 0 && end < length) end += 1;

  return Math.max(
    0,
    _maxProductWithoutZero(nums.slice(start, end)),
    maxProduct(nums.slice(end + 1))
  );
};

// 思路三: 参考leetcode网友的牛逼答案
// 不断更新正的最大值，以及负的最小值
const maxProduct2 = nums => {
  if (nums.length === 0) return -Infinity;
  if (nums.length === 1) return nums[0];

  let posMax = nums[0];
  let negMax = nums[0];
  let ret = nums[0];
  for (let i = 1; i < nums.length; i += 1) {
    const tempPosMax = posMax;
    const tempNegMax = negMax;
    posMax = Math.max(nums[i], nums[i] * tempPosMax, nums[i] * tempNegMax);
    negMax = Math.min(nums[i], nums[i] * tempPosMax, nums[i] * tempNegMax);
    ret = Math.max(posMax, ret);
  }
  return ret;
};

